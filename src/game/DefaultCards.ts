export const getCardsByLevel = (level: 'soft' | 'hot' | 'spicy'): GameCard[] => {
  return defaultCards.filter(card => {
    if (level === 'spicy') return true; // Spicy includes all levels
    if (level === 'hot') return card.level === 'soft' || card.level === 'hot';
    return card.level === 'soft';
  });
};

export const getRandomCard = (level: 'soft' | 'hot' | 'spicy', excludeIds: string[] = []): GameCard => {
  const availableCards = getCardsByLevel(level).filter(card => !excludeIds.includes(card.id));
  const randomIndex = Math.floor(Math.random() * availableCards.length);
  return availableCards[randomIndex];
};

export const shuffleCards = (cards: GameCard[]): GameCard[] => {
  const shuffled = [...cards];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};